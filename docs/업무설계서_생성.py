def set_ko(run):
    run.font.name = FONT
    run._element.rPr.rFonts.set(qn('w:eastAsia'), FONT)

def h(text, level=1):
    p = doc.add_heading(level=level)
    r = p.add_run(text)
    set_ko(r)
    r.font.color.rgb = NAVY
    return p

def para(text, bold=False, color=None, size=10.5, italic=False, space_after=6):
    p = doc.add_paragraph()
    r = p.add_run(text)
    set_ko(r); r.bold = bold; r.italic = italic; r.font.size = Pt(size)
    if color: r.font.color.rgb = color
    p.paragraph_format.space_after = Pt(space_after)
    return p

def bullet(text, level=0):
    p = doc.add_paragraph(style='List Bullet' if level == 0 else 'List Bullet 2')
    r = p.add_run(text); set_ko(r)
    return p

def num(text):
    p = doc.add_paragraph(style='List Number')
    r = p.add_run(text); set_ko(r)
    return p

def table(headers, rows, widths=None):
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = 'Light Grid Accent 1'
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, hd in enumerate(t.rows[0].cells):
        hd.text = ''
        r = hd.paragraphs[0].add_run(headers[i]); set_ko(r); r.bold = True; r.font.size = Pt(9.5)
    for row in rows:
        cells = t.add_row().cells
        for i, v in enumerate(cells):
            v.text = ''
            r = v.paragraphs[0].add_run(str(row[i])); set_ko(r); r.font.size = Pt(9.5)
    if widths:
        for i, w in enumerate(widths):
            for row in t.rows:
                row.cells[i].width = Cm(w)
    doc.add_paragraph().paragraph_format.space_after = Pt(2)
    return t

