def set_ko(r, font=FONT):
    r.font.name = font
    for tag in ('w:ascii', 'w:hAnsi', 'w:eastAsia'):
        r._element.rPr.rFonts.set(qn(tag), font)


def shade(cell, fill):
    shd = OxmlElement('w:shd'); shd.set(qn('w:val'), 'clear'); shd.set(qn('w:fill'), fill)
    cell._tc.get_or_add_tcPr().append(shd)


def h(text, level=1):
    p = doc.add_heading(level=level); r = p.add_run(text); set_ko(r)
    r.font.color.rgb = NAVY if level <= 2 else RGBColor(0x33, 0x44, 0x66)
    p.paragraph_format.space_before = Pt(10 if level == 1 else 6); return p


def para(text, color=None, italic=False, size=10.5, after=6):
    p = doc.add_paragraph(); r = p.add_run(text); set_ko(r); r.italic = italic; r.font.size = Pt(size)
    if color: r.font.color.rgb = color
    p.paragraph_format.space_after = Pt(after); return p


def b(text, level=0):
    p = doc.add_paragraph(style='List Bullet' if level == 0 else 'List Bullet 2')
    p.paragraph_format.space_after = Pt(2)
    if ' :: ' in text:
        head, rest = text.split(' :: ', 1)
        r = p.add_run(head); set_ko(r); r.bold = True
        r2 = p.add_run(' — ' + rest); set_ko(r2)
    else:
        r = p.add_run(text); set_ko(r)
    return p


def num(items):
    for t in items:
        p = doc.add_paragraph(style='List Number'); p.paragraph_format.space_after = Pt(2)
        if ' :: ' in t:
            head, rest = t.split(' :: ', 1)
            r = p.add_run(head); set_ko(r); r.bold = True
            r2 = p.add_run(' — ' + rest); set_ko(r2)
        else:
            r = p.add_run(t); set_ko(r)


def quote(text):
    p = doc.add_paragraph(); p.paragraph_format.left_indent = Cm(0.4); p.paragraph_format.space_after = Pt(6)
    pPr = p._p.get_or_add_pPr(); shd = OxmlElement('w:shd'); shd.set(qn('w:fill'), BOXFILL); pPr.append(shd)
    r = p.add_run('  ' + text); set_ko(r); r.bold = True; r.font.color.rgb = NAVY; return p


def table(headers, rows, widths=None, fs=9.5, zebra=True):
    t = doc.add_table(rows=1, cols=len(headers)); t.style = 'Table Grid'
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, c in enumerate(t.rows[0].cells):
        c.text = ''; p = c.paragraphs[0]; p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r = p.add_run(headers[i]); set_ko(r); r.bold = True; r.font.size = Pt(fs); r.font.color.rgb = WHITE
        shade(c, HEADFILL)
    for ri, row in enumerate(rows):
        cells = t.add_row().cells
        for i, c in enumerate(cells):
            c.text = ''; r = c.paragraphs[0].add_run(str(row[i])); set_ko(r); r.font.size = Pt(fs)
            if zebra and ri % 2 == 1: shade(c, ZEBRA)
    if widths:
        for i, w in enumerate(widths):
            for row in t.rows: row.cells[i].width = Cm(w)
    doc.add_paragraph().paragraph_format.space_after = Pt(2)


